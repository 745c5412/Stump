using System.Collections.Generic;
using System.Linq;
using Stump.Core.IO;
using Stump.ORM;
using Stump.ORM.SubSonic.SQLGeneration.Schema;
using Stump.Server.WorldServer.Game.Guilds;
using Stump.Server.WorldServer.Game.Maps;

namespace Stump.Server.WorldServer.Database.World
{
    public class WorldMapPaddockRelator
    {
        public static string FetchQuery = "SELECT * FROM world_maps_paddock";
    }

    [TableName("world_maps_paddock")]
    public sealed class WorldMapPaddockRecord : IAutoGeneratedRecord
    {
        private Guild m_guild;
        private Map m_map;
        private List<int> m_paddockedMounts = new List<int>();
        private string m_paddockedMountsCSV;
        private List<int> m_stabledMounts = new List<int>();
        private string m_stabledMountsCSV;

        [PrimaryKey("Id")]
        public int Id
        {
            get;
            set;
        }

        public int GuildId
        {
            get;
            set;
        }

        [Ignore]
        public Guild Guild
        {
            get
            {
                if (GuildId == -1)
                    return null;

                return m_guild ?? (m_guild = GuildManager.Instance.TryGetGuild(GuildId));
            }
            set
            {
                m_guild = value;

                if (value == null)
                    GuildId = -1;
                else
                    GuildId = value.Id;
            }
        }

        public int? MapId
        {
            get;
            set;
        }

        [Ignore]
        public Map Map
        {
            get
            {
                if (!MapId.HasValue)
                    return null;

                return m_map ?? (m_map = Game.World.Instance.GetMap(MapId.Value));
            }
            set
            {
                m_map = value;

                if (value == null)
                    MapId = null;
                else
                    MapId = value.Id;
            }
        }

        public uint MaxOutdoorMount
        {
            get;
            set;
        }

        public uint MaxItems
        {
            get;
            set;
        }

        public bool Abandonned
        {
            get;
            set;
        }

        public bool OnSale
        {
            get;
            set;
        }

        public bool Locked
        {
            get;
            set;
        }

        public int Price
        {
            get;
            set;
        }

        [Ignore]
        public List<int> StabledMounts
        {
            get
            {
                return m_stabledMounts;
            }
            set
            {
                m_stabledMounts = value;
                m_stabledMountsCSV = m_stabledMounts.ToCSV(",");
            }
        }

        [NullString]
        public string StabledMountsCSV
        {
            get
            {
                return m_stabledMountsCSV;
            }
            set
            {
                m_stabledMountsCSV = value;
                m_stabledMounts = !string.IsNullOrEmpty(m_stabledMountsCSV) ? m_stabledMountsCSV.FromCSV<int>(",").ToList() : new List<int>();
            }
        }

        [Ignore]
        public List<int> PaddockedMounts
        {
            get
            {
                return m_paddockedMounts;
            }
            set
            {
                m_paddockedMounts = value;
                m_paddockedMountsCSV = m_paddockedMounts.ToCSV(",");
            }
        }

        [NullString]
        public string PaddockedMountsCSV
        {
            get
            {
                return m_paddockedMountsCSV;
            }
            set
            {
                m_paddockedMountsCSV = value;
                m_paddockedMounts = !string.IsNullOrEmpty(m_paddockedMountsCSV) ? m_paddockedMountsCSV.FromCSV<int>(",").ToList() : new List<int>();
            }
        }
    }
}
