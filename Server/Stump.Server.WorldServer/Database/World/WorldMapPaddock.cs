using Stump.ORM;
using Stump.ORM.SubSonic.SQLGeneration.Schema;
using Stump.Server.WorldServer.Game.Guilds;
using Stump.Server.WorldServer.Game.Maps;

namespace Stump.Server.WorldServer.Database.World
{
    public class WorldMapPaddockRelator
    {
        public static string FetchQuery = "SELECT * FROM world_maps_paddock";
    }

    [TableName("world_maps_paddock")]
    public sealed class WorldMapPaddockRecord : IAutoGeneratedRecord
    {
        private Guild m_guild;
        private Map m_map;

        [PrimaryKey("Id")]
        public int Id
        {
            get;
            set;
        }

        public int GuildId
        {
            get;
            set;
        }

        [Ignore]
        public Guild Guild
        {
            get
            {
                if (GuildId == -1)
                    return null;

                return m_guild ?? (m_guild = GuildManager.Instance.TryGetGuild(GuildId));
            }
            set
            {
                m_guild = value;

                if (value == null)
                    GuildId = -1;
                else
                    GuildId = value.Id;
            }
        }

        public int? MapId
        {
            get;
            set;
        }

        [Ignore]
        public Map Map
        {
            get
            {
                if (!MapId.HasValue)
                    return null;

                return m_map ?? (m_map = Game.World.Instance.GetMap(MapId.Value));
            }
            set
            {
                m_map = value;

                if (value == null)
                    MapId = null;
                else
                    MapId = value.Id;
            }
        }

        public uint MaxOutdoorMount
        {
            get;
            set;
        }

        public uint MaxItems
        {
            get;
            set;
        }

        public bool Abandonned
        {
            get;
            set;
        }

        public bool OnSale
        {
            get;
            set;
        }

        public bool Locked
        {
            get;
            set;
        }

        public int Price
        {
            get;
            set;
        }
    }
}
