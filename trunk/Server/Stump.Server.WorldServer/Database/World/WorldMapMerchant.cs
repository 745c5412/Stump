using System;
using Stump.DofusProtocol.D2oClasses.Tools.D2o;
using Stump.DofusProtocol.D2oClasses;
using Stump.ORM;
using Stump.ORM.SubSonic.SQLGeneration.Schema;
using Stump.Server.WorldServer.Game.Maps;

namespace Stump.Server.WorldServer.Database.World
{
    public class WorldMapMerchantRelator
    {
        public static string FetchQuery = "SELECT * FROM world_maps_merchant";
    }

    [TableName("world_maps_merchant")]
    public sealed class WorldMapMerchantRecord : IAutoGeneratedRecord
    {
        private Map m_map;

        [PrimaryKey("Id", false)]
        public int Id
        {
            get;
            set;
        }

        public int CharacterId
        {
            get;
            set;
        }

        public int? MapId
        {
            get;
            set;
        }

        [Ignore]
        public Map Map
        {
            get
            {
                if (!MapId.HasValue)
                    return null;

                return m_map ?? (m_map = Game.World.Instance.GetMap(MapId.Value));
            }
            set
            {
                m_map = value;

                if (value == null)
                    MapId = null;
                else
                    MapId = value.Id;
            }
        }

        public int Cell
        {
            get;
            set;
        }

        public int Direction
        {
            get;
            set;
        }

        public int EntityLook
        {
            get;
            set;
        }

        public string Name
        {
            get;
            set;
        }

        public int SellType
        {
            get;
            set;
        }

        public int MerchantSince
        {
            get;
            set;
        }
    }
}