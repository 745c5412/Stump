using Stump.ORM;
using Stump.ORM.SubSonic.SQLGeneration.Schema;
using Stump.Server.WorldServer.Game.Maps;
using Stump.DofusProtocol.Types;
using Stump.DofusProtocol.Types.Extensions;

namespace Stump.Server.WorldServer.Database.World
{
    public class WorldMapMerchantRelator
    {
        public static string FetchQuery = "SELECT * FROM world_maps_merchant";
    }

    [TableName("world_maps_merchant")]
    public sealed class WorldMapMerchantRecord : IAutoGeneratedRecord
    {
        private Map m_map;
        private EntityLook m_entityLook;
        private string m_lookAsString;

        [PrimaryKey("Id", false)]
        public int Id
        {
            get;
            set;
        }

        public int CharacterId
        {
            get;
            set;
        }

        public int? MapId
        {
            get;
            set;
        }

        [Ignore]
        public Map Map
        {
            get
            {
                if (!MapId.HasValue)
                    return null;

                return m_map ?? (m_map = Game.World.Instance.GetMap(MapId.Value));
            }
            set
            {
                m_map = value;

                if (value == null)
                    MapId = null;
                else
                    MapId = value.Id;
            }
        }

        public int Cell
        {
            get;
            set;
        }

        public int Direction
        {
            get;
            set;
        }

        public string LookAsString
        {
            get
            {
                if (m_entityLook == null)
                    return string.Empty;

                if (string.IsNullOrEmpty(m_lookAsString))
                    m_lookAsString = Look.ConvertToString();

                return m_lookAsString;
            }
            set
            {
                m_lookAsString = value;

                if (value != null)
                    m_entityLook = m_lookAsString.ToEntityLook();
            }
        }

        [Ignore]
        public EntityLook Look
        {
            get { return m_entityLook; }
            set
            {
                m_entityLook = value;

                if (value != null)
                    m_lookAsString = value.ConvertToString();
            }
        }

        public string Name
        {
            get;
            set;
        }

        public int SellType
        {
            get;
            set;
        }

        public int MerchantSince
        {
            get;
            set;
        }
    }
}