using System;
using Stump.DofusProtocol.Enums;
using Stump.ORM;
using Stump.ORM.SubSonic.SQLGeneration.Schema;

namespace Stump.Server.WorldServer.Database.Guilds
{
    public class GuildMemberRelator
    {
        public static string FetchQuery = "SELECT * FROM guild_members";

        /// <summary>
        /// Use string.Format
        /// </summary>
        public static string FetchByGuildId = "SELECT * FROM guild_members gm LEFT JOIN characters ch ON ch.Id = gm.CharacterId WHERE GuildId={0}";
        /// <summary>
        /// Use string.Format
        /// </summary>
        public static string FindByCharacterId = "SELECT * FROM guild_members gm LEFT JOIN characters ch ON ch.Id = gm.CharacterId WHERE CharacterId={0}";
    }

    [TableName("guild_members")]
    public class GuildMemberRecord : IAutoGeneratedRecord
    {
        [PrimaryKey("CharacterId", false)]
        public int CharacterId
        {
            get;
            set;
        }

        public int AccountId
        {
            get;
            set;
        }

        public int GuildId
        {
            get;
            set;
        }

        public short RankId
        {
            get;
            set;
        }

        public GuildRightsBitEnum Rights
        {
            get;
            set;
        }

        public long GivenExperience
        {
            get;
            set;
        }

        public byte GivenPercent
        {
            get;
            set;
        }
        
        [Ignore]
        public string Name
        {
            get;
            set;
        }

        [Ignore]
        public byte Level
        {
            get;
            set;
        }

        [Ignore]
        public PlayableBreedEnum Breed
        {
            get;
            set;
        }

        [Ignore]
        public SexTypeEnum Sex
        {
            get;
            set;
        }

        [Ignore]
        public AlignmentSideEnum AlignementSide
        {
            get;
            set;
        }

        [Ignore]
        public DateTime LastConnection
        {
            get;
            set;
        }
    }
}
