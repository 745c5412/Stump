using System;
using System.Collections.Generic;
using System.Linq;
using Stump.Core.IO;
using Stump.ORM;
using Stump.ORM.SubSonic.SQLGeneration.Schema;

namespace Stump.Server.WorldServer.Database.Guilds
{
    public class GuildRelator
    {
        public static string FetchQuery = "SELECT * FROM guilds";
        /// <summary>
        /// Use string.Format
        /// </summary>
        public static string FetchById = "SELECT * FROM guilds WHERE Id={0}";
    }

    [TableName("guilds")]
    public class GuildRecord : IAutoGeneratedRecord
    {
        [PrimaryKey("Id", false)]
        public int Id
        {
            get;
            set;
        }

        [Ignore]
        public bool IsNew
        {
            get;
            set;
        }

        public DateTime CreationDate
        {
            get;
            set;
        }

        public String Name
        {
            get;
            set;
        }

        public long Experience
        {
            get;
            set;
        }

        public short Boost
        {
            get;
            set;
        }

        public short Prospecting
        {
            get;
            set;
        }

        public short Wisdom
        {
            get;
            set;
        }

        public short Pods
        {
            get;
            set;
        }

        public sbyte MaxTaxCollectors
        {
            get;
            set;
        }

        private List<sbyte> m_spells = new List<sbyte>();
        private string m_spellsCSV;

        [Ignore]
        public List<sbyte> Spells
        {
            get
            {
                return m_spells;
            }
            set
            {
                m_spells = value;
                m_spellsCSV = m_spells.ToCSV(",");
            }
        }

        [NullString]
        public string SpellsCSV
        {
            get
            {
                return m_spellsCSV;
            }
            set
            {
                m_spellsCSV = value;
                m_spells = !string.IsNullOrEmpty(m_spellsCSV) ? m_spellsCSV.FromCSV<sbyte>(",").ToList() : new List<sbyte>();
            }
        }

        public short EmblemBackgroundShape
        {
            get;
            set;
        }

        public int EmblemBackgroundColor
        {
            get;
            set;
        }

        public short EmblemForegroundShape
        {
            get;
            set;
        }

        public int EmblemForegroundColor
        {
            get;
            set;
        }
    }
}
