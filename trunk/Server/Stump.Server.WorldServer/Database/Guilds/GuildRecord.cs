using System;
using System.Collections.Generic;
using Stump.DofusProtocol.Enums;
using Stump.DofusProtocol.Types;
using Stump.DofusProtocol.Types.Extensions;
using Stump.ORM;
using Stump.ORM.SubSonic.SQLGeneration.Schema;
using Stump.Server.WorldServer.Database.Breeds;
using Stump.Server.WorldServer.Game.Maps;

namespace Stump.Server.WorldServer.Database.Guilds
{
    [TableName("guilds")]
    public class GuildRecord : IAutoGeneratedRecord
    {
        private GuildEmblem m_guildEmblem;
        private string m_guildEmblemString;

        public int Id
        {
            get;
            set;
        }

        public DateTime CreationDate
        {
            get;
            set;
        }

        public String Name
        {
            get;
            set;
        }

        public int Level
        {
            get;
            set;
        }

        public double Experience
        {
            get;
            set;
        }

        [Ignore]
        public GuildEmblem GuildEmblem
        {
            get { return m_guildEmblem; }
            set
            {
                m_guildEmblem = value;
                m_guildEmblemString = value.ConvertToString();
            }
        }

        [NullString]
        public string GuildEmblemString
        {
            get { return m_guildEmblemString; }
            set
            {
                m_guildEmblemString = value;
                m_guildEmblem = !string.IsNullOrEmpty(GuildEmblemString) ? m_guildEmblemString.ToGuildEmblem() : null;
            }
        }
    }
}
