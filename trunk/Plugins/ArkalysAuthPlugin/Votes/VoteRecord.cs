using System;
using Stump.ORM;
using Stump.ORM.SubSonic.SQLGeneration.Schema;
using Stump.Server.AuthServer;
using Stump.Server.BaseServer.Initialization;

namespace ArkalysAuthPlugin.Votes
{
    public static class VoteRecordRegister
    {
        [Initialization(InitializationPass.Database)]
        public static void Initialize()
        {
            AuthServer.Instance.DBAccessor.AddMappedType(typeof (VoteRecord));
        }
    }

    [TableName("Votes")]
    public class VoteRecord : IAutoGeneratedRecord
    {
        [PrimaryKey("Id")]
        public int Id
        {
            get;
            set;
        }

        [Column("AccID")]
        public int AccountId
        {
            get;
            set;
        }

        [Column("Date")]
        public DateTime VoteDate
        {
            get;
            set;
        }

        [Column("IPAddress")]
        public string IP
        {
            get;
            set;
        }
    }
}